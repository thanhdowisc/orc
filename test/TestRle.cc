  TEST(RLEv1, splitRuns) {
    SeekableInputStream* stream = 
      new SeekableArrayInputStream({0x7d, 0x01, 0xff, 0x01, 0xfb, 0x01,
	    0x02, 0x03, 0x04, 0x05});
    std::unique_ptr<orc::RleDecoder> rle = 
      orc::createRleDecoder(std::move(std::unique_ptr<orc::SeekableInputStream>
				      (stream)), 
			    false, orc::VERSION_1);
    long* data = new long[200];
    for(int i=0; i < 42; ++i) {
      rle->next(data, 3, 0);
      for(int j=0; j < 3; ++j) {
	EXPECT_EQ(255 + i * 3 + j, data[j]) << "Wrong output at " << i 
					    << ", " << j;
      }
    }
    rle->next(data, 3, 0);
    EXPECT_EQ(381, data[0]);
    EXPECT_EQ(382, data[1]);
    EXPECT_EQ(1, data[2]);
    rle->next(data, 3, 0);
    EXPECT_EQ(2, data[0]);
    EXPECT_EQ(3, data[1]);
    EXPECT_EQ(4, data[2]);
    rle->next(data, 1, 0);
    EXPECT_EQ(5, data[0]);
    delete[] data;
  }

  TEST(RLEv1, testSigned) {
    SeekableInputStream* stream = 
      new SeekableArrayInputStream({0x7f, 0x01, 0x00});
    std::unique_ptr<orc::RleDecoder> rle = 
      orc::createRleDecoder(std::move(std::unique_ptr<orc::SeekableInputStream>
				      (stream)), 
			    true, orc::VERSION_1);
    long* data = new long[100];
    rle->next(data, 100, 0);
    for(int i=0; i < 100; ++i) {
      if (i % 2 == 0) {
	EXPECT_EQ(i / 2, data[i]) << "Wrong output at " << i;
      } else {
	EXPECT_EQ((i+1) / -2, data[i]) << "Wrong output at " << i;
      }
    }
    rle->next(data, 30, 0);
    for(int i=0; i < 30; ++i) {
      if (i % 2 == 0) {
	EXPECT_EQ((i + 100) / 2, data[i]) << "Wrong output at " << (i + 100);
      } else {
	EXPECT_EQ((i+101) / -2, data[i]) << "Wrong output at " << (i + 100);
      }
    }
    delete[] data;
  }

  TEST(RLEv1, testNull) {
    SeekableInputStream* stream = 
      new SeekableArrayInputStream({0x7f, 0x02, 0x00});
    std::unique_ptr<orc::RleDecoder> rle = 
      orc::createRleDecoder(std::move(std::unique_ptr<orc::SeekableInputStream>
				      (stream)), 
			    true, orc::VERSION_1);
    long* data = new long[25];
    bool* isNull = new bool[25];
    for(int i=0; i < 25; ++i) {
      isNull[i] = i % 2;
    }
    for(int i=0; i < 10; ++i) {
      rle->next(data, 25, isNull);
      for(int j=0; j < 25; ++j) {
	EXPECT_EQ(j % 2, isNull[j]);
	if (!isNull[j]) {
	  EXPECT_EQ(i * 13 + j/2, data[j]);
	}
      }
    }
    delete[] data;
  }

